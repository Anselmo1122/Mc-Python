
# Variables en Python

# Definiendo una variable
nombre = "Anselmo"

saludo = "Hola"

#Concatenar con f-strings
bienvenida = f"{saludo} {nombre}!!!"

print(bienvenida)

edad = "18"

# Concatenar con (+)
print("Tu edad es: " + edad)

# operadores de pertenencia (in / not in)

print("Anselmo" in bienvenida) # True
print("Hola" not in bienvenida) # False

